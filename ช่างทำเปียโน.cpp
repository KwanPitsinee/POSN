#include <bits/stdc++.h>
using namespace std;

int main() {
    int n,r;
  cin>>n;
  r=n%7;
  if(r==1){
    cout<<"C";
  }
  else if(r==2){
    cout<<"D";
  }
  else if(r==3){
    cout<<"E";
  }
  else if(r==4){
    cout<<"F";
  }
  else if(r==5){
    cout<<"G";
  }
  else if(r==6){
    cout<<"A";
  }
  else{
    cout<<"B";
  }
}  
